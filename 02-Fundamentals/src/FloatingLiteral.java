
public class FloatingLiteral {

	public static void main(String[] args) {
		
		System.out.println(10.66F);
		System.out.println(13.567f);
		System.out.println(12.67);
		System.out.println(13.45d);
		System.out.println(0.6);
		System.out.println(145.0);
		System.out.println(1.0E3);
		System.out.println(1.0E-2);

	}

}
